// script.js
let diseases = [];
let editIndex = null;

document.getElementById("addButton").addEventListener("click", function() {
    const diseaseInput = document.getElementById("diseaseInput");
    const causeInput = document.getElementById("causeInput");
    const treatmentInput = document.getElementById("treatmentInput");
    
    const diseaseValue = diseaseInput.value.trim();
    const causeValue = causeInput.value.trim();
    const treatmentValue = treatmentInput.value.trim();

    if (editIndex !== null) {
        // Editing an existing disease
        diseases[editIndex] = { disease: diseaseValue, cause: causeValue, treatment: treatmentValue };
        editIndex = null; // Reset edit index
    } else {
        // Adding a new disease
        if (diseaseValue && causeValue && treatmentValue) {
            diseases.push({ disease: diseaseValue, cause: causeValue, treatment: treatmentValue });
        }
    }

    diseaseInput.value = ''; // Clear input
    causeInput.value = '';
    treatmentInput.value = '';
    displayDiseases();
});

function displayDiseases() {
    const diseaseList = document.getElementById("diseaseList");
    diseaseList.innerHTML = ''; // Clear the list

    diseases.forEach((item, index) => {
        const li = document.createElement("li");
        li.innerHTML = `<strong>${item.disease}</strong><br>Cause: ${item.cause}<br>Treatment: ${item.treatment}`;

        const editButton = document.createElement("button");
        editButton.innerHTML = '<i class="fas fa-edit"></i>';
        editButton.classList.add("edit-button");
        editButton.addEventListener("click", () => editDisease(index));

        const deleteButton = document.createElement("button");
        deleteButton.innerHTML = '<i class="fas fa-trash"></i>';
        deleteButton.classList.add("delete-button");
        deleteButton.addEventListener("click", () => deleteDisease(index));

        li.appendChild(editButton);
        li.appendChild(deleteButton);
        diseaseList.appendChild(li);
    });
}

function editDisease(index) {
    const diseaseInput = document.getElementById("diseaseInput");
    const causeInput = document.getElementById("causeInput");
    const treatmentInput = document.getElementById("treatmentInput");

    diseaseInput.value = diseases[index].disease; // Set input to the disease being edited
    causeInput.value = diseases[index].cause; // Set input to the cause
    treatmentInput.value = diseases[index].treatment; // Set input to the treatment
    editIndex = index; // Set edit index
}

function deleteDisease(index) {
    diseases.splice(index, 1); // Remove the disease from the array
    displayDiseases(); // Refresh the display
}
