function showDiseaseInfo() {
    const select = document.getElementById("regionSelect");
    const infoDiv = document.getElementById("diseaseInfo");
    const selectedRegion = select.value;

    if (selectedRegion) {
        const diseases = diseaseData[selectedRegion].diseases;
        let htmlContent = `<h2>Common Diseases in ${select.options[select.selectedIndex].text}</h2><br>`;

        diseases.forEach(disease => {
            htmlContent += `
                <div><h4>${disease.name}</h4></div>
                <div id="test">
                    <img src="${disease.image}" alt="${disease.name}">
                    <p>${disease.description}</p>
                 
                </div>
                <div>
                   <p>${disease.resources}</p>
                </div>
            `;
        });

        infoDiv.innerHTML = htmlContent;
        infoDiv.style.display = "block";
    } else {
        infoDiv.style.display = "none";
    }
}


